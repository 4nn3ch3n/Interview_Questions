package com.fdmgroup.interviewquestions.exceptions;

public class IQQuestionException extends IQStorageException
{
	private static final long serialVersionUID = -4935532878364154568L;

	public IQQuestionException() {}
	
	public IQQuestionException(String message)
	{
		super(message);
	}
	
	public IQQuestionException(Throwable cause)
	{
		super(cause);
	}
	
	public IQQuestionException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
