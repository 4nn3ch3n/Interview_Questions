package com.fdmgroup.interviewquestions.exceptions;

public class IQStorageException extends Exception
{
	private static final long serialVersionUID = 7538425765060051462L;

	public IQStorageException(){}
	
	public IQStorageException(String message)
	{
		super(message);
	}
	
	public IQStorageException(Throwable cause)
	{
		super(cause);
	}
	
	public IQStorageException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
